# Descrição: Vamos receber dois dados diferentes do usuário e concatená-los em uma única string

# Recebe os dados do usuário
dado1 = input("Digite o primeiro dado: ")
dado2 = input("Digite o segundo dado: ")

# Concatena os dados
resultado = dado1 + dado2

# Exibe o resultado
print("Resultado da concatenação:", resultado)

# Concatena os dados com separação
resultado_com_separacao = dado1 + " " + dado2

# Exibe o resultado com separação
print("Resultado da concatenação com separação:", resultado_com_separacao)